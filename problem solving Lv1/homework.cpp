
using namespace std;
#include <iostream>

	int main()
	{
		int A, B;
		cout << "please enter the first number A" <<"\n";
	cin >> A;
	cout << "please enter the second number B"<<"\n";
	cin >> B;

	cout << A << " + " << B << " = " << A + B <<endl;
	cout << A << " - " << B << " = " << A - B <<endl;
	cout << A << " * " << B << " = " << A * B <<endl;
	cout << A << " / " << B << " = " << A / B <<endl;
	cout << A << " % " << B << " = " << A % B <<endl;
	return 0;

}